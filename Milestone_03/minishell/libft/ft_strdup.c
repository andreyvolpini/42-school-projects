#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;

	i = (int)ft_strlen(s);
	str = malloc(sizeof(char) * i + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
